package entidad;

public class Provincia {

	protected int id;
	protected String nombre;	
	
	public Provincia() 
    {
		
    }

	public int getId() 
	{
		return id;
	}

	public void setId(int id) 
	{
		this.id = id;
	}

	public String getNombre() 
	{
		return nombre;
	}

	public void setNombre(String nombre) 
	{
		this.nombre = nombre;
	}	
}
